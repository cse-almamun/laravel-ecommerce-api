     */
    public function store(Request $request)
    {
        $id = Auth::user()->id;

        //create orders
        $order = Order::create([
            "user_id" => $id,
            "items" => json_encode($request->items),
            'subtotal' => $request->subtotal,
            'coupon' => $request->coupon,
            'discount' => $request->discount,
            'total' => $request->total
        ]);
        return $this->success(['order' => $order]);
    }

